def mood_response(mood):
    if mood == 'happy':
        print("That's great! Whatever you're doing keep it up.")
    elif mood == 'sad':
        print("I hope you find something to brighten your day.")
    elif mood == 'frustrated':
        print("For your own mental health do something that makes you feel like you've made some progress.")
    elif mood == 'ecstatic':
        print("Write down what you're doing cuz you'll want to repeat it.")
    else:
        print("We do not have a response for that but I hope it feels good.")